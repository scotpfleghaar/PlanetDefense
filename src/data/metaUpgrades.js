// Permanent upgrades bought with Salvage. Unlimited levels; cost grows exponentially per level.
// expCost(base, growth) returns a cost(l) function: base * growth^l, rounded to a clean number.
function expCost(base, growth) {
  return l => Math.round(base * Math.pow(growth, l) / 5) * 5;
}

